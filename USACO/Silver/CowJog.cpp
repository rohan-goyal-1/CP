// check for overflow (long long vs int / make everything long long)
// index out of bounds can cause program to work locally but won't on grading server
//
// Solution Ideas:
//  - Linear search
//  - Binary search
//  - Unordered_... data structures

int main () {
#ifndef LOCAL
    setIO("cowjog");
#else
    fastIO;
#endif

    int n, t; cin >> n >> t;
    vll pos(n);
    for (int i = 0; i < n; i++) {
        ll a, b; cin >> a >> b;
        pos[i] = a + b * t;
    }

    ll ans = 0, last = pos[n - 1];
    for (ll i = n - 1; i >= 0; i--) {
        if (pos[i] < last) ans++;
        last = min(last, pos[i]);
    }
    cout << ++ans << nl;

    return 0;
}
