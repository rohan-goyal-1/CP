int main () {
    fastIO;
#ifdef LOCAL
    clock_t tStart = clock();
#endif

    int n, x; cin >> n >> x;
    vi P(n); for (int& p : P) cin >> p;
    sort(all(P));
    int l = 0, r = n - 1;
    int ans = 0;
    while (l <= r) {
        if (P[l] + P[r] > x) { ans++; r--; }
        else { ans++; l++, r--; }
    }
    cout << ans << nl;

#ifdef LOCAL
    cerr << fixed << setprecision(10) << "\nTime Taken: " << (double)(clock() - tStart) / CLOCKS_PER_SEC << '\n';
#endif

    return 0;
}
